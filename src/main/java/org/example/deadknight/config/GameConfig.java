package org.example.deadknight.config;

/**
 * Глобальная конфигурация игры.
 * <p>
 * Хранит статические настройки, влияющие на поведение игры
 * (например, отладочный режим или «режим бога»).
 * <br>
 * Класс является утилитарным и не предназначен для создания экземпляров.
 */
public final class GameConfig {

    /**
     * Приватный конструктор предотвращает создание экземпляра.
     */
    private GameConfig() {}

    /**
     * Флаг отладки хитбоксов.
     * <p>
     * Если {@code true}, то на экране отображаются границы хитбоксов
     * (например, для тестирования коллизий).
     */
    public static boolean DEBUG_HITBOXES = false;

    /**
     * Режим бессмертия.
     * <p>
     * Если {@code true}, игрок не получает урон и становится неуязвимым.
     */
    public static boolean GOD_MODE = false;
}
